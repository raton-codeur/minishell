#include "libft.h"

char	*skip_zeros(char *s)
{
	int		i;
	char	*result;

	i = 0;
	result = s;
	while (s[i] == '0')
	{
		result++;
		i++;
	}
	if (s[i] == '\0')
		result--;
	return (result);
}
